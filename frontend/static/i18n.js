    const normalize = (lang) => {
        const raw = String(lang || 'en').toLowerCase();
        if (raw.startsWith('hi')) return 'hi';
        if (raw.startsWith('te')) return 'te';
        return 'en';
    };

    const getLanguage = () => normalize(localStorage.getItem('app_language') || 'en');

    const setLanguage = (lang) => {
        const normalized = normalize(lang);
        localStorage.setItem('app_language', normalized);
        document.dispatchEvent(new CustomEvent('app-language-changed', { detail: { language: normalized } }));
        return normalized;
    };

    const t = (key, fallback = '') => {
        const lang = getLanguage();
        return (DICT[lang] && DICT[lang][key]) || (DICT.en && DICT.en[key]) || fallback || key;
    };

    const applyPageTranslations = () => {
        document.querySelectorAll('[data-i18n]').forEach((el) => {
            el.textContent = t(el.getAttribute('data-i18n'), el.textContent);
        });
        document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
            el.setAttribute('placeholder', t(el.getAttribute('data-i18n-placeholder'), el.getAttribute('placeholder') || ''));
        });
    };

    window.AppI18n = {
        getLanguage,
        setLanguage,
        t,
        applyPageTranslations,
    };
